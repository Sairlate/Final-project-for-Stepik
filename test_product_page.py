import pytest
from pages.basket_page import BasketPage
from pages.product_page import ProductPage
import time


def est_guest_can_add_product_to_basket(browser):
    link = "http://selenium1py.pythonanywhere.com/catalogue/coders-at-work_207/"
    page = ProductPage(browser, link)
    page.open()
    page.add_product_to_basket()
    browser.implicitly_wait(10)  # для будущих проверяющих, если вдруг страницы будут долго грузиться
    page.solve_quiz_and_get_code()
    browser.implicitly_wait(10)
    page.product_name_matches_with_added_product()
    browser.implicitly_wait(10)
    page.product_price_matches_with_cart_price()


